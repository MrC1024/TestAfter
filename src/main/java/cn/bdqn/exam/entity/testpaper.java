package cn.bdqn.exam.entity;

public class testpaper {
    private int tid;
    private String tname;
    private int testpaperstepId;
    private String testpaperstepSite;
    private String testpaperstepName;

    public String getTestpaperstepName() {
        return testpaperstepName;
    }

    public void setTestpaperstepName(String testpaperstepName) {
        this.testpaperstepName = testpaperstepName;
    }

    public int getTid() {
        return tid;
    }

    public void setTid(int tid) {
        this.tid = tid;
    }

    public String getTname() {
        return tname;
    }

    public void setTname(String tname) {
        this.tname = tname;
    }

    public int getTestpaperstepId() {
        return testpaperstepId;
    }

    public void setTestpaperstepId(int testpaperstepId) {
        this.testpaperstepId = testpaperstepId;
    }

    public String getTestpaperstepSite() {
        return testpaperstepSite;
    }

    public void setTestpaperstepSite(String testpaperstepSite) {
        this.testpaperstepSite = testpaperstepSite;
    }
}
